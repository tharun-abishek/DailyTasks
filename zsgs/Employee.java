package org.task1.zsgs;

public class Employee {
	    int empid;
	    String name;
	    String role;
	    int experience;
	    String department;
	    String contact;
	   public Employee(int id , String name, String role , int experience , String department, String contact){
	       empid = id;
	       this.name = name;
	       this.role = role;
	       this.experience = experience;
	       this.department = department;
	       this.contact = contact;
	   }  
	  
	
}
